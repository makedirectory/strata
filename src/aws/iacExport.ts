/** A resource resolved to its export identifiers, before serialization. */
interface PlannedResource {
  resource: ResourceInstance;
  /** Sanitised CloudFormation logical id (alphanumeric, unique). */
  logicalId: string;
  /** Sanitised Terraform resource name (valid identifier, unique). */
  tfName: string;
}

/** Sanitise to a CloudFormation logical id: alphanumeric, leading letter. */
function sanitizeLogicalId(raw: string): string {
  const cleaned = raw.replace(/[^A-Za-z0-9]/g, "");
  if (!cleaned) return "Resource";
  return /^[A-Za-z]/.test(cleaned) ? cleaned : `R${cleaned}`;
}

/** Sanitise to a Terraform identifier: [A-Za-z0-9_-], leading letter/underscore. */
function sanitizeTfName(raw: string): string {
  const cleaned = raw.replace(/[^A-Za-z0-9_-]/g, "_");
  if (!cleaned) return "resource";
  return /^[A-Za-z_]/.test(cleaned) ? cleaned : `_${cleaned}`;
}

/** Make `base` unique within `used`, appending a numeric suffix if needed. */
function unique(base: string, used: Set<string>): string {
  let candidate = base;
  let n = 2;
  while (used.has(candidate)) candidate = `${base}${n++}`;
  used.add(candidate);
  return candidate;
}

/** Assign stable, unique export identifiers to every resource (id-sorted). */
function planResources(graph: InfrastructureGraph): PlannedResource[] {
  const sorted = [...graph.resources].sort((a, b) => a.id.localeCompare(b.id));
  const cfnUsed = new Set<string>();
  const tfUsed = new Set<string>();
  return sorted.map((resource) => ({
    resource,
    logicalId: unique(sanitizeLogicalId(resource.name || resource.id), cfnUsed),
    tfName: unique(sanitizeTfName(resource.name || resource.id), tfUsed),
  }));
}

/**
 * Outgoing dependency target ids for a resource: its containment parent plus
 * every relationship it originates, restricted to resources present in `ids`.
 * (Typed edge *kinds* are intentionally coarsened to dependencies — a
 * documented lossy delta; structure is preserved, semantics are not.)
 */
function dependencyTargets(
  resource: ResourceInstance,
  graph: InfrastructureGraph,
  ids: Set<string>,
): string[] {
  const targets = new Set<string>();
  if (resource.parentId && ids.has(resource.parentId) && resource.parentId !== resource.id) {
    targets.add(resource.parentId);
  }
  for (const rel of graph.relationships) {
    if (rel.from === resource.id && ids.has(rel.to) && rel.to !== resource.id) {
      targets.add(rel.to);
    }
  }
  return [...targets].sort();
}

/** Known config (under registry keys) + a `report` side-effect for missing required fields. */
function knownConfig(
  resource: ResourceInstance,
  address: string,
  report: ExportReport,
): { values: Record<string, unknown>; todoKeys: string[] } {
  const svc = getService(resource.serviceId);
  const values: Record<string, unknown> = {};
  const todoKeys: string[] = [];
  if (!svc) return { values, todoKeys };
  for (const field of svc.configFields) {
    const has = Object.prototype.hasOwnProperty.call(resource.config, field.key);
    if (has && resource.config[field.key] !== "" && resource.config[field.key] !== undefined) {
      values[field.key] = resource.config[field.key];
    } else if (field.required) {
      todoKeys.push(field.key);
      report.todos.push({ address, field: field.key });
    }
  }
  return { values, todoKeys };
}

function emptyReport(): ExportReport {
  return { exported: 0, faithful: 0, skipped: [], todos: [], warnings: [] };
}

/** An extra CloudFormation resource a transform asks the exporter to emit. */
interface CfnAuxResource {
  /** Appended to the primary logical id to form the aux logical id. */
  logicalSuffix: string;
  type: string;
  properties: Record<string, unknown>;
}

/** Properties for the primary resource, plus any auxiliary resources. */
interface CfnScaffold {
  properties: Record<string, unknown>;
  auxiliary?: CfnAuxResource[];
}

/**
 * Per-service CloudFormation property transformers (scaffold path only).
 *
 * A flat key-rename (`cfnPropertyNames`) can only relabel a key — it cannot
 * change a value's *shape* or split config across *multiple* resources. Some
 * services need exactly that to emit valid CloudFormation, so they register a
 * transformer here. It receives the resource's known config (registry keys) and
 * returns real CFN `Properties` plus any auxiliary resources to emit alongside
 * (each gets a `DependsOn` back to the primary).
 *
 * Only the scaffold path consults this map; a faithfully-captured `raw` source
 * is always re-emitted verbatim and never transformed.
 */
const CFN_TRANSFORMS: Record<string, (config: Record<string, unknown>) => CfnScaffold> = {
  // S3: versioning/encryption are nested objects and Block Public Access is a
  // SEPARATE AWS::S3::BucketPublicAccessBlock resource — none expressible as a
  // flat rename. (`storageClass` is object/lifecycle-level, not a bucket prop,
  // so it is intentionally dropped from the scaffold.)
  "s3-bucket": (config) => {
    const properties: Record<string, unknown> = {};
    if (typeof config.bucketName === "string" && config.bucketName) {
      properties.BucketName = config.bucketName;
    }
    if (config.versioning !== undefined) {
      properties.VersioningConfiguration = { Status: config.versioning ? "Enabled" : "Suspended" };
    }
    if (typeof config.encryption === "string" && config.encryption) {
      const algo =
        config.encryption === "SSE-KMS"
          ? "aws:kms"
          : config.encryption === "DSSE-KMS"
            ? "aws:kms:dsse"
            : "AES256";
      properties.BucketEncryption = {
        ServerSideEncryptionConfiguration: [
          { ServerSideEncryptionByDefault: { SSEAlgorithm: algo } },
        ],
      };
    }
    const auxiliary: CfnAuxResource[] = [];
    if (config.blockPublicAccess !== undefined) {
      const on = config.blockPublicAccess === true;
      auxiliary.push({
        logicalSuffix: "PublicAccessBlock",
        type: "AWS::S3::BucketPublicAccessBlock",
        properties: {
          Bucket: { Ref: "__PRIMARY__" },
          PublicAccessBlockConfiguration: {
            BlockPublicAcls: on,
            BlockPublicPolicy: on,
            IgnorePublicAcls: on,
            RestrictPublicBuckets: on,
          },
        },
      });
    }
    return { properties, auxiliary };
  },
};

