/**
 * Strata — Infrastructure-as-Code export
 * ------------------------------------------------
 * The mirror of `iac.ts`: turns the in-app `InfrastructureGraph` into
 * **CloudFormation** (JSON / YAML) and **Terraform** (HCL).
 *
 * Honest framing (see specs/flow-3-export-iac.md):
 *   Import is lossy — we normalise to our model, drop unmapped types, and keep
 *   only registry-known config. So export is a **scaffold a human finishes**,
 *   not faithful round-trip. Generated IaC has the right resource *types* and
 *   *dependencies*, but property names follow Strata's config model (not the
 *   full provider schema), and required provider fields we don't model are
 *   emitted as `TODO` placeholders and tallied in the `ExportReport`.
 *
 * Join keys (reverse of the importer):
 *  - CloudFormation: a service's registry `cfnType` IS the resource `Type`.
 *  - Terraform: the registry `serviceId` maps to a canonical `aws_*` type via
 *    the inverse of `TF_TYPE_TO_SERVICE_ID` (first-listed wins; see below).
 *
 * Pure and deterministic: no network, no auth, no Date/Math.random. Resources
 * are processed in a stable id-sorted order so output is byte-stable for tests.
 */
import yaml from "js-yaml";
import type { InfrastructureGraph, ResourceInstance } from "./model";
import { getService } from "./registry";
import { TF_TYPE_TO_SERVICE_ID } from "./iac";

export type ExportFormat = "cloudformation-json" | "cloudformation-yaml" | "terraform";

/** Coverage/honesty report — mirrors `IacImportResult`'s shape. */
export interface ExportReport {
  /** Number of resources emitted. */
  exported: number;
  /**
   * Of `exported`, how many were re-emitted **verbatim** from a captured
   * `raw` source (faithful round-trip) vs. scaffolded from Strata's model.
   * `exported - faithful` resources are scaffolds the user must finish.
   */
  faithful: number;
  /** Resources omitted because the registry has no target type for them. */
  skipped: { id: string; serviceId: string; reason: string }[];
  /** Required provider fields we don't model, emitted as placeholders. */
  todos: { address: string; field: string }[];
  /** Non-fatal notes surfaced to the user. */
  warnings: string[];
}

const HEADER_NOTE =
  "Generated by Strata. This is a scaffold, not deploy-ready IaC: property names " +
  "follow Strata's model and TODO markers flag fields you must complete.";

/**
 * serviceId → canonical Terraform type. The import table is many-to-one
 * (e.g. aws_lb / aws_alb / aws_elb all map to "elastic-load-balancer"), so the
 * inverse keeps the FIRST-listed Terraform type as the canonical export target.
 */
export const SERVICE_ID_TO_TF_TYPE: Record<string, string> = (() => {
  const inv: Record<string, string> = {};
  for (const [tfType, serviceId] of Object.entries(TF_TYPE_TO_SERVICE_ID)) {
    if (!(serviceId in inv)) inv[serviceId] = tfType;
  }
  return inv;
})();

