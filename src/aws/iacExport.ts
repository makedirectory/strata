// Shared planning
// ---------------------------------------------------------------------------

/** A resource resolved to its export identifiers, before serialization. */
interface PlannedResource {
  resource: ResourceInstance;
  /** Sanitised CloudFormation logical id (alphanumeric, unique). */
  logicalId: string;
  /** Sanitised Terraform resource name (valid identifier, unique). */
  tfName: string;
}

/** Sanitise to a CloudFormation logical id: alphanumeric, leading letter. */
function sanitizeLogicalId(raw: string): string {
  const cleaned = raw.replace(/[^A-Za-z0-9]/g, "");
  if (!cleaned) return "Resource";
  return /^[A-Za-z]/.test(cleaned) ? cleaned : `R${cleaned}`;
}

/** Sanitise to a Terraform identifier: [A-Za-z0-9_-], leading letter/underscore. */
function sanitizeTfName(raw: string): string {
  const cleaned = raw.replace(/[^A-Za-z0-9_-]/g, "_");
  if (!cleaned) return "resource";
  return /^[A-Za-z_]/.test(cleaned) ? cleaned : `_${cleaned}`;
}

/** Make `base` unique within `used`, appending a numeric suffix if needed. */
function unique(base: string, used: Set<string>): string {
  let candidate = base;
  let n = 2;
  while (used.has(candidate)) candidate = `${base}${n++}`;
  used.add(candidate);
  return candidate;
}

/** Assign stable, unique export identifiers to every resource (id-sorted). */
function planResources(graph: InfrastructureGraph): PlannedResource[] {
  const sorted = [...graph.resources].sort((a, b) => a.id.localeCompare(b.id));
  const cfnUsed = new Set<string>();
  const tfUsed = new Set<string>();
  return sorted.map((resource) => ({
    resource,
    logicalId: unique(sanitizeLogicalId(resource.name || resource.id), cfnUsed),
    tfName: unique(sanitizeTfName(resource.name || resource.id), tfUsed),
  }));
}

/**
 * Outgoing dependency target ids for a resource: its containment parent plus
 * every relationship it originates, restricted to resources present in `ids`.
 * (Typed edge *kinds* are intentionally coarsened to dependencies — a
 * documented lossy delta; structure is preserved, semantics are not.)
 */
function dependencyTargets(
  resource: ResourceInstance,
  graph: InfrastructureGraph,
  ids: Set<string>,
): string[] {
  const targets = new Set<string>();
  if (resource.parentId && ids.has(resource.parentId) && resource.parentId !== resource.id) {
    targets.add(resource.parentId);
  }
  for (const rel of graph.relationships) {
    if (rel.from === resource.id && ids.has(rel.to) && rel.to !== resource.id) {
      targets.add(rel.to);
    }
  }
  return [...targets].sort();
}

/** Known config (under registry keys) + a `report` side-effect for missing required fields. */
function knownConfig(
  resource: ResourceInstance,
  address: string,
  report: ExportReport,
): { values: Record<string, unknown>; todoKeys: string[] } {
  const svc = getService(resource.serviceId);
  const values: Record<string, unknown> = {};
  const todoKeys: string[] = [];
  if (!svc) return { values, todoKeys };
  for (const field of svc.configFields) {
    const has = Object.prototype.hasOwnProperty.call(resource.config, field.key);
    if (has && resource.config[field.key] !== "" && resource.config[field.key] !== undefined) {
      values[field.key] = resource.config[field.key];
    } else if (field.required) {
      todoKeys.push(field.key);
      report.todos.push({ address, field: field.key });
    }
  }
  return { values, todoKeys };
}

function emptyReport(): ExportReport {
  return { exported: 0, faithful: 0, skipped: [], todos: [], warnings: [] };
}

// ---------------------------------------------------------------------------
// CloudFormation
// ---------------------------------------------------------------------------

export interface CloudFormationExport {
  json: string;
  yaml: string;
  report: ExportReport;
}

export function exportCloudFormation(graph: InfrastructureGraph): CloudFormationExport {
  const report = emptyReport();
  const planned = planResources(graph);
  const ids = new Set(planned.map((p) => p.resource.id));
  const logicalById = new Map(planned.map((p) => [p.resource.id, p.logicalId]));

  const resources: Record<string, unknown> = {};
  for (const { resource, logicalId } of planned) {
    const svc = getService(resource.serviceId);
    // A captured CloudFormation `raw` source re-emits faithfully even if the
    // registry entry has since lost its cfnType, so resolve the emitted Type
    // from `raw` first, falling back to the registry.
    const faithfulRaw = resource.raw?.format === "cloudformation" ? resource.raw : undefined;
    const cfnType = faithfulRaw?.type ?? svc?.cfnType;
    if (!cfnType) {
      report.skipped.push({
        id: resource.id,
        serviceId: resource.serviceId,
        reason: "no CloudFormation type in the registry",
      });
      continue;
    }

    const computedDependsOn = dependencyTargets(resource, graph, ids)
      .map((id) => logicalById.get(id))
      .filter((v): v is string => Boolean(v));

    let entry: Record<string, unknown>;
    if (faithfulRaw) {
      // Faithful path: re-emit the verbatim Type + Properties (intrinsics intact).
      entry = { Type: faithfulRaw.type };
      if (faithfulRaw.properties && Object.keys(faithfulRaw.properties).length > 0) {
        entry.Properties = faithfulRaw.properties;
      }
      if (faithfulRaw.condition) entry.Condition = faithfulRaw.condition;
      if (faithfulRaw.metadata) entry.Metadata = faithfulRaw.metadata;
      // Prefer the original DependsOn; fall back to the graph-derived deps.
      const dependsOn = faithfulRaw.dependsOn?.length ? faithfulRaw.dependsOn : computedDependsOn;
      if (dependsOn.length > 0) entry.DependsOn = dependsOn;
      report.faithful++;
    } else {
      // Scaffold path: property names follow Strata's model (with the optional
      // cfnPropertyNames map applied) and required-missing fields become TODOs.
      const { values, todoKeys } = knownConfig(resource, logicalId, report);
      const renamed = applyPropertyNames(svc, values);
      const properties: Record<string, unknown> = { ...renamed };
      for (const key of todoKeys) {
        properties[svc?.cfnPropertyNames?.[key] ?? key] = "TODO: required — set this value";
      }
      entry = { Type: cfnType };
      if (Object.keys(properties).length > 0) entry.Properties = properties;
      if (computedDependsOn.length > 0) entry.DependsOn = computedDependsOn;
    }

    resources[logicalId] = entry;
    report.exported++;
  }

  if (report.exported === 0) {
    report.warnings.push("No resources could be exported to CloudFormation.");
  }
  if (report.skipped.length > 0) {
    report.warnings.push(`${report.skipped.length} resource(s) skipped (no CloudFormation type).`);
  }
  const scaffolded = report.exported - report.faithful;
  if (report.faithful > 0 && scaffolded > 0) {
    report.warnings.push(
      `${report.faithful} resource(s) re-emitted faithfully; ${scaffolded} scaffolded from Strata's model.`,
    );
  }

  // Re-emit captured template sections (Parameters/Outputs/…) for faithful
  // round-trip; otherwise fall back to a minimal scaffold header.
  const src = graph.iacSource?.format === "cloudformation" ? graph.iacSource : undefined;
  const template: Record<string, unknown> = {
    AWSTemplateFormatVersion: src?.formatVersion ?? "2010-09-09",
    Description: src?.description ?? HEADER_NOTE,
  };
  if (src?.parameters) template.Parameters = src.parameters;
  if (src?.mappings) template.Mappings = src.mappings;
  if (src?.conditions) template.Conditions = src.conditions;
  if (src?.metadata) template.Metadata = src.metadata;
  if (src?.transform !== undefined) template.Transform = src.transform;
  template.Resources = resources;
  if (src?.outputs) template.Outputs = src.outputs;

  return {
    json: JSON.stringify(template, null, 2),
    yaml: yaml.dump(template, { lineWidth: 120, noRefs: true }),
    report,
  };
}

/**
 * Apply a service's optional `cfnPropertyNames` map to rename Strata config
 * keys to their provider-native property names (CFN Stage 3 / multi-cloud).
 * Keys without a mapping pass through unchanged.
 */
function applyPropertyNames(
  svc: ReturnType<typeof getService>,
  values: Record<string, unknown>,
): Record<string, unknown> {
  const map = svc?.cfnPropertyNames;
  if (!map) return values;
  const out: Record<string, unknown> = {};
  for (const [k, v] of Object.entries(values)) out[map[k] ?? k] = v;
  return out;
}

// ---------------------------------------------------------------------------
// Terraform (HCL)
// ---------------------------------------------------------------------------

export interface TerraformExport {
  hcl: string;
  report: ExportReport;
}

/** True if `k` is a bare HCL identifier (else it must be quoted as a key). */
function isBareKey(k: string): boolean {
  return /^[A-Za-z_][A-Za-z0-9_-]*$/.test(k);
}

/** Render a value as an HCL literal (strings quoted, nested lists/maps indented). */
function hclValue(value: unknown, indent: string): string {
  if (value === null || value === undefined) return "null";
  if (typeof value === "string") return JSON.stringify(value);
  if (typeof value === "number" || typeof value === "boolean") return String(value);
  if (Array.isArray(value)) {
    if (value.length === 0) return "[]";
    const inner = value.map((v) => `${indent}  ${hclValue(v, `${indent}  `)}`).join(",\n");
    return `[\n${inner},\n${indent}]`;
  }
  if (typeof value === "object") {
    const entries = Object.entries(value as Record<string, unknown>);
    if (entries.length === 0) return "{}";
    const inner = entries
      .map(([k, v]) => {
        const key = isBareKey(k) ? k : JSON.stringify(k);
        return `${indent}  ${key} = ${hclValue(v, `${indent}  `)}`;
      })
      .join("\n");
    return `{\n${inner}\n${indent}}`;
  }
  return JSON.stringify(String(value));
}

export function exportTerraform(
  graph: InfrastructureGraph,
  serviceIdToTfType: Record<string, string> = SERVICE_ID_TO_TF_TYPE,
): TerraformExport {
  const report = emptyReport();
  const planned = planResources(graph);
  const ids = new Set(planned.map((p) => p.resource.id));
  const addressById = new Map<string, string>();
  for (const p of planned) {
    const tfType = serviceIdToTfType[p.resource.serviceId];
    if (tfType) addressById.set(p.resource.id, `${tfType}.${p.tfName}`);
  }

  const blocks: string[] = [];
  for (const { resource, tfName } of planned) {
    const tfType = serviceIdToTfType[resource.serviceId];
    if (!tfType) {
      report.skipped.push({
        id: resource.id,
        serviceId: resource.serviceId,
        reason: "no Terraform type mapping in the registry",
      });
      continue;
    }
    const address = `${tfType}.${tfName}`;
    const { values, todoKeys } = knownConfig(resource, address, report);

    const lines: string[] = [`resource "${tfType}" "${tfName}" {`];
    for (const [key, value] of Object.entries(values)) {
      const k = isBareKey(key) ? key : JSON.stringify(key);
      lines.push(`  ${k} = ${hclValue(value, "  ")}`);
    }
    for (const key of todoKeys) lines.push(`  # TODO: set required field "${key}"`);
    const deps = dependencyTargets(resource, graph, ids)
      .map((id) => addressById.get(id))
      .filter((v): v is string => Boolean(v));
    if (deps.length > 0) lines.push(`  depends_on = [${deps.join(", ")}]`);
    lines.push("}");
    blocks.push(lines.join("\n"));
    report.exported++;
  }

  if (report.exported === 0) report.warnings.push("No resources could be exported to Terraform.");
  if (report.skipped.length > 0) {
    report.warnings.push(`${report.skipped.length} resource(s) skipped (no Terraform mapping).`);
  }

  const header = `# ${HEADER_NOTE}\n`;
  return { hcl: report.exported > 0 ? `${header}\n${blocks.join("\n\n")}\n` : header, report };
}

// ---------------------------------------------------------------------------
// Convenience
// ---------------------------------------------------------------------------

export interface IacExportResult {
  content: string;
  /** Suggested download filename. */
  filename: string;
  report: ExportReport;
}

/** Export to the chosen format, returning content + filename + report. */
export function exportIaC(graph: InfrastructureGraph, format: ExportFormat): IacExportResult {
  switch (format) {
    case "cloudformation-json": {
      const { json, report } = exportCloudFormation(graph);
      return { content: json, filename: "strata-template.json", report };
    }
    case "cloudformation-yaml": {
      const { yaml: y, report } = exportCloudFormation(graph);
      return { content: y, filename: "strata-template.yaml", report };
    }
    case "terraform": {
      const { hcl, report } = exportTerraform(graph);
      return { content: hcl, filename: "strata.tf", report };
    }
  }
}
