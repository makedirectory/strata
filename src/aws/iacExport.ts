export interface IacExportResult {
  content: string;
  /** Suggested download filename. */
  filename: string;
  report: ExportReport;
}

/** Export to the chosen format, returning content + filename + report. */
export function exportIaC(graph: InfrastructureGraph, format: ExportFormat): IacExportResult {
  switch (format) {
    case "cloudformation-json": {
      const { json, report } = exportCloudFormation(graph);
      return { content: json, filename: "strata-template.json", report };
    }
    case "cloudformation-yaml": {
      const { yaml: y, report } = exportCloudFormation(graph);
      return { content: y, filename: "strata-template.yaml", report };
    }
    case "terraform": {
      const { hcl, report } = exportTerraform(graph);
      return { content: hcl, filename: "strata.tf", report };
    }
  }
}
